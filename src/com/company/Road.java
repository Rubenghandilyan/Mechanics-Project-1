package com.company;

public class Road {
    private int dt;
    private int ds;

    public Road(int dt, int ds) {
        this.dt = dt;
        this.ds = ds;
    }

    public int getDt() {
        return dt;
    }

    public void setDt(int dt) {
        this.dt = dt;
    }

    public int getDs() {
        return ds;
    }

    public void setDs(int ds) {
        this.ds = ds;
    }
}
